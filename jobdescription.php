        <div class="abcontent">
            	<p class="txt">A Logistician organizes the safe, efficient movement of goods on behalf of an exporter, importer or another company or person, sometimes including dealing with packing and storage.</p></br>
                <p class="txt">Taking into account the type of goods and the customers' delivery requirements, freight forwarders arrange the best means of transport, using the services of shipping lines, airlines or road and rail freight operators. In some cases, the freight forwarding company itself provides the service.</p></br>
                <p class="txt">Companies vary in size and type, from those operating on a national and international basis to smaller, more specialized firms, who deal with particular types of goods or operate within particular geographical areas</p></br>
                <p class="txt">Typical activities include:</p></br>
                <p>
                <ul>
                	<li>Researching and planning the most appropriate route for a shipment (taking into account of the perishable or hazardous nature of the goods, cost, transit time and security);</li>
                    <li>Arranging appropriate packing (taking into account of climate, terrain, weight, nature of goods and cost) and delivery or warehousing of goods at their final destination;</li>
                    <li>Obtaining, checking and preparing documentation to meet customs and insurance requirements, packing specifications, and compliance with overseas countries' regulations and fiscal regimes;</li>
                    <li>Offering consolidation services by air, sea and road - ensuring cost effective and secure solutions to small shippers with insufficient cargo to utilise their own dedicated units;</li>
                    <li>Liaising with third parties to move goods (by road, rail, air or sea) in accordance with customer requirements;</li>
                    <li>Arranging insurance and assisting the client in the event of a claim;</li>
                    <li>Offering tailored IT solutions and EDI (electronic data interchange) connections;</li>
                    <li>Arranging payment of freight and other charges, or collection of payment on behalf of the client;</li>
                    <li>Transmitting data by internet and satellite systems, enabling real-time tracking and tracing of goods;</li>
                    <li>Arranging air transport for urgent and high-value freight and managing the risk door to door;</li>
                    <li>Arranging charters for large volume, out-of-gauge or project movements by air and sea;</li>
                    <li>Acting as broker in customs negotiations worldwide to guide the freight efficiently through complex procedures;</li>
                    <li>Dealing with special arrangements for transporting delicate cargoes, such as livestock, food, medical supplies and other fragile goods;</li>
                    <li>Arranging courier and specialist hand-carry services;</li>
                    <li>Working closely with customers, colleagues and third parties to ensure smooth operations to deadlines;</li>
                    <li>Maintaining visibility and control through all phases of the journey, including the production of management reports and statistical and unit cost analysis;</li>
                    <li>Acting as consultant in customs matters;</li>
                    <li>Maintaining current knowledge of relevant legislation, political situations and other factors that could affect the movement of freight </li>
                    </ul></p>
